// Loops- use to repeatedly execute a block of condition until a specific condition is way. It is a way to automate reprtitivenet

// 1. For loop 
// i++ this is needed if not the print will go indefinitely
for  (let i = 0; i <  5 ; i++) {
    console.log("For loop count:", i)

}

// 2. while loop: more control over the increment
let count=1;
while (count<=5) {
    
    console.log("While loop Count : ", count);
    count++;
}

//  3. Do While loop: it runs the block of code at least once and then checks the condition

let x=1;
do{
    console.log('Do While loop Count ', x);
    x ++;
}while(x <=5)

